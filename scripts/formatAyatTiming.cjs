// الملفات المطلوب تعديلها
const files = [
  'Al-Juhaynee.json',
  'Hudhaify.json',
  'Shuraym.json',
  'sudais.json'
];

const basePath = path.join(__dirname, '../public/json/ayat_Timming');

files.forEach(file => {
  const filePath = path.join(basePath, file);
  const raw = fs.readFileSync(filePath, 'utf8');
  const data = JSON.parse(raw);

  // احتفظ بالبيانات العامة
  const { id, reciter } = data;
  const newData = { id, reciter, surahs: [] };

  // لكل سورة
  for (let i = 1; i <= 114; i++) {
    const surahKey = String(i);
    const timing = data[surahKey];
    if (!timing) continue;

    newData.surahs.push({
      number: surahKey,
      name: surahNames[i - 1],
      verses_count: surahVerseCounts[i - 1],
      Timing: timing
    });
  }

  fs.writeFileSync(filePath, JSON.stringify(newData, null, 2), 'utf8');
  console.log(`تم تنسيق الملف: ${file}`);
});