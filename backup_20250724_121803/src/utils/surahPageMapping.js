/**
 * الحصول على رقم الصفحة لسورة معينة
 * @param {number} surahNumber - رقم السورة
 * @returns {number} رقم الصفحة
 */
export const getSurahPage = (surahNumber) => {
  return surahToPageMapping[surahNumber] || 1;
};

/**
 * الحصول على السور الموجودة في صفحة معينة
 * @param {number} pageNumber - رقم الصفحة
 * @returns {number[]} أرقام السور في هذه الصفحة
 */
export const getSurahsInPage = (pageNumber) => {
  const surahs = [];
  
  for (const [surahNum, page] of Object.entries(surahToPageMapping)) {
    if (parseInt(page) === pageNumber) {
      surahs.push(parseInt(surahNum));
    }
  }
  
  return surahs;
};

export default surahToPageMapping;
